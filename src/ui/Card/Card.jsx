// Импортируем стили из CSS-модуля для компонента Card
import styles from './Card.module.scss';

/**
 * Компонент Card - переиспользуемая стеклянная (glass) панель
 * Используется как контейнер для контента с glass-эффектом
 * Поддерживает передачу дополнительных классов и свойств
 * @param {Object} props - Свойства компонента
 * @param {React.ReactNode} props.children - Дочерние React-элементы для отображения внутри карточки
 * @param {string} props.className - Дополнительные CSS классы для кастомизации внешнего вида
 * @param {Object} props.props - Остальные свойства (передаются в элемент div)
 */
const Card = ({ children, className = '', ...props }) => {
  // Возвращаем JSX разметку компонента Card
  return (
    // Базовый div элемент с применением стилей карточки
    <div 
      // Формируем строку классов: базовый класс карточки + пользовательские классы
      className={`${styles.card} ${className}`}
      // Передаем все остальные свойства (aria-*, data-*, и т.д.)
      {...props}
    >
      {/* Отображаем дочерние элементы внутри карточки */}
      {children}
    </div>
  );
};

// Экспортируем компонент Card для использования в других частях приложения
export default Card;




